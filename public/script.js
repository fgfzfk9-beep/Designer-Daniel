function saveContentChanges() {
    const t = document.getElementById('editTitle').value;
    const d = document.getElementById('editDesc').value;
    const p = document.getElementById('editPhone').value;
    
    const fileInput = document.getElementById('editProfileImg');
    if(fileInput.files && fileInput.files[0]) {
        const reader = new FileReader();
        reader.onload = function(e) {
            tempProfileImg = e.target.result;
            localStorage.setItem('profileImg', tempProfileImg);
            updateProfileDisplay();
            finalizeContentSave(t, d, p);
        }
        reader.readAsDataURL(fileInput.files[0]);
    } else {
        finalizeContentSave(t, d, p);
    }
}

function finalizeContentSave(t, d, p) {
    if(t) { document.getElementById('heroTitle').innerHTML = t.replace(/\n/g, '<br>'); localStorage.setItem('siteTitle', t); }
    if(d) { document.getElementById('heroDesc').innerText = d; localStorage.setItem('siteDesc', d); }
    if(p) { document.getElementById('phoneNum') && (document.getElementById('phoneNum').innerText = p); localStorage.setItem('sitePhone', p); }
    
    alert(currentLang === 'ar' ? 'تم حفظ تعديلات المحتوى بنجاح!' : currentLang === 'en' ? 'Content saved successfully!' : 'ناوەڕۆک بە سەرکەوتوویی پاشەکەوتکرا!');
}

