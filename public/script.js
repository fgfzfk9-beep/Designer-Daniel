const sectionIds = ['home', 'stats', 'portfolio', 'skills'];
const sectionNames = { 'home': 'القسم الرئيسي', 'stats': 'الإحصائيات', 'portfolio': 'معرض الأعمال', 'skills': 'المهارات التقنية' };

function initSortableList() {
    const list = document.getElementById('sortableSections');
    list.innerHTML = '';
    let currentOrder = JSON.parse(localStorage.getItem('sectionOrder')) || sectionIds;
    
    currentOrder.forEach(id => {
        const li = document.createElement('li');
        li.className = 'sortable-item'; li.draggable = true; li.dataset.id = id;
        li.innerHTML = `<span>${sectionNames[id]}</span><i class="fas fa-grip-vertical drag-handle"></i>`;
        li.addEventListener('dragstart', dragStart);
        li.addEventListener('dragover', e => e.preventDefault());
        li.addEventListener('drop', drop);
        li.addEventListener('dragenter', function() { this.style.borderColor = 'var(--neon)'; });
        li.addEventListener('dragleave', function() { this.style.borderColor = 'var(--border)'; });
        list.appendChild(li);
    });
}

let draggedItem = null;
function dragStart(e) { draggedItem = this; e.dataTransfer.effectAllowed = 'move'; setTimeout(() => this.style.opacity = '0.5', 0); }
function drop(e) {
    e.preventDefault(); this.style.borderColor = 'var(--border)'; draggedItem.style.opacity = '1';
    if (this !== draggedItem) {
        const list = document.getElementById('sortableSections');
        const allItems = [...list.children];
        const draggedIdx = allItems.indexOf(draggedItem);
        const droppedIdx = allItems.indexOf(this);
        if (draggedIdx < droppedIdx) this.after(draggedItem); else this.before(draggedItem);
    }
}

function saveLayoutChanges() {
    const newOrder = [...document.getElementById('sortableSections').children].map(item => item.dataset.id);
    localStorage.setItem('sectionOrder', JSON.stringify(newOrder));
    applySectionOrder(newOrder);
    alert(currentLang === 'ar' ? 'تم حفظ ترتيب الأقسام!' : currentLang === 'en' ? 'Layout saved!' : 'ڕیزبەندی پاشەکەوتکرا!');
}

function applySectionOrder(order) {
    const main = document.getElementById('mainContent');
    order.forEach(id => { const section = document.getElementById(id); if(section) main.appendChild(section); });
}

