async function sendMessage() {
    const input = document.getElementById('userInput');
    const msg = input.value.trim();
    if(!msg) return;
    
    const chatBody = document.getElementById('chatBody');
    chatBody.innerHTML += `<div class="msg user">${msg}</div>`;
    input.value = '';
    chatBody.scrollTop = chatBody.scrollHeight;
    
    try {
        const response = await fetch('/api/chatbot', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({ message: msg, lang: currentLang })
        });

        const data = await response.json();

        setTimeout(() => {
            chatBody.innerHTML += `<div class="msg bot">${data.reply}</div>`;
            chatBody.scrollTop = chatBody.scrollHeight;
        }, 500);

    } catch (error) {
        console.error("خطأ في الاتصال بباك اند البوت:", error);
        setTimeout(() => {
            let errorMsg = currentLang === 'ar' ? "عذراً، حدث خطأ في الاتصال بالخادم!" : currentLang === 'en' ? "Sorry, server connection error!" : "ببوورە، کێشەیەک لە پەیوەندی سێرڤەر هەیە!";
            chatBody.innerHTML += `<div class="msg bot">${errorMsg}</div>`;
            chatBody.scrollTop = chatBody.scrollHeight;
        }, 500);
    }
}

// === نظام تعدد اللغات ===
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('siteLang', lang);
    document.documentElement.dir = lang === 'en' ? 'ltr' : 'rtl';
    document.documentElement.lang = lang;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if(translations[lang][key]) {
            if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });
    
    document.querySelectorAll('.lang-switcher button').forEach(btn => {
        btn.classList.remove('active-lang');
        if(btn.textContent.toLowerCase() === lang) btn.classList.add('active-lang');
    });
    
    const chatBody = document.getElementById('chatBody');
    if(chatBody.children.length <= 1) {
        chatBody.innerHTML = `<div class="msg bot">${translations[lang].chat_welcome}</div>`;
    }
}

