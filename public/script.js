async function checkLogin() {
    const adminPassInput = document.getElementById('adminPass').value;

    try {
        const response = await fetch('/api/login', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({ password: adminPassInput })
        });

        const data = await response.json();

        if (data.success) {
            localStorage.setItem('isAdminLoggedIn', 'true');
            
            // تنبيه نجاح الدخول الاحترافي
            Swal.fire({
                icon: 'success',
                title: currentLang === 'ar' ? 'تم تسجيل الدخول بنجاح' : 'Logged in successfully',
                background: '#13131a',
                color: '#fff',
                confirmButtonColor: '#6366f1',
                timer: 2000,
                showConfirmButton: false
            });

            showPanel();
            loadEditableContent();
            initSortableList();
            renderAdminProjectsList();
        } else {
            // تنبيه الرمز الخاطئ الاحترافي
            Swal.fire({
                icon: 'error',
                title: currentLang === 'ar' ? 'الرمز السري غير صحيح!' : 'Wrong password!',
                text: currentLang === 'ar' ? 'يرجى إعادة المحاولة' : 'Please try again',
                background: '#13131a',
                color: '#fff',
                confirmButtonColor: '#ef4444'
            });
            document.getElementById('adminPass').value = '';
        }

    } catch (error) {
        console.error("خطأ:", error);
        // تنبيه فشل الاتصال بالخادم الاحترافي
        Swal.fire({
            icon: 'warning',
            title: currentLang === 'ar' ? 'فشل الاتصال بالخادم!' : 'Connection failed!',
            text: currentLang === 'ar' ? 'تأكد من تشغيل الباك اند الخاص بك أونلاين' : 'Make sure the backend is running',
            background: '#13131a',
            color: '#fff',
            confirmButtonColor: '#f59e0b'
        });
    }
}

