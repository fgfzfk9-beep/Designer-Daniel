// === دالة حفظ تعديلات المحتوى في قاعدة البيانات ===
async function finalizeContentSave(t, d, p) {
    if(t) document.getElementById('heroTitle').innerHTML = t.replace(/\n/g, '<br>');
    if(d) document.getElementById('heroDesc').innerText = d;
    if(p && document.getElementById('phoneNum')) document.getElementById('phoneNum').innerText = p;
    
    try {
        await fetch('/api/save-data', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ title: t, desc: d, phone: p, projects: projects })
        });
        alert(currentLang === 'ar' ? 'تم حفظ تعديلات المحتوى في قاعدة البيانات!' : 'Content saved to database!');
    } catch (error) {
        console.error("خطأ في الحفظ:", error);
    }
}

